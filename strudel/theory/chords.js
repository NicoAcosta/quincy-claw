// === Common Progressions by Genre ===

// -- Pop/Rock --
// I-V-vi-IV    (C G Am F)         — most popular pop progression
// I-IV-V       (C F G)            — rock basics
// vi-IV-I-V    (Am F C G)         — emotional pop
// I-vi-IV-V    (C Am F G)         — 50s doo-wop

// -- Jazz --
// ii-V-I       (Dm7 G7 Cmaj7)    — fundamental jazz cadence
// I-vi-ii-V    (Cmaj7 Am7 Dm7 G7) — turnaround
// iii-vi-ii-V  (Em7 Am7 Dm7 G7)  — rhythm changes bridge
// I-IV-iii-vi  (Cmaj7 Fmaj7 Em7 Am7) — smooth jazz
// bIII-bVI-bVII-I (Ebmaj7 Abmaj7 Bbmaj7 Cmaj7) — chromatic approach

// -- Lo-fi Hip Hop --
// ii7-V7-Imaj7      (Dm7 G7 Cmaj7)    — jazz-influenced
// Imaj7-vi7-ii7-V7  (Cmaj7 Am7 Dm7 G7) — mellow turnaround
// im7-iv7-VII7-IIImaj7 (Cm7 Fm7 Bb7 Ebmaj7) — minor wandering
// IVmaj7-iii7-vi7-ii7  (Fmaj7 Em7 Am7 Dm7) — descending smooth

// -- House --
// im-iv-VII-III  (Am Dm G C)      — classic minor house
// im-VI-VII-III  (Am F G C)       — uplifting
// im-iim-v-im   (Am Bm Em Am)    — dark minimal

// -- Techno --
// Single chord drones or two-chord oscillation
// im-bVII       (Am G)            — dark, driving
// im-iv         (Am Dm)           — hypnotic

// -- Ambient --
// Imaj7-IVmaj7  (Cmaj7 Fmaj7)    — open, floating
// Isus2-Vsus4   (Csus2 Gsus4)    — ethereal
// Free modal movement — no strict progressions

// -- Trap --
// im-bVI-bVII   (Fm Db Eb)       — dark, cinematic
// im-iv-bVI-bVII (Fm Bbm Db Eb)  — atmospheric


// Scale Reference for Strudel
// Use with .scale("Root:ScaleName") e.g. .scale("C:minor")

// === Common Scales ===
// major          — W W H W W W H (happy, bright)
// minor          — W H W W H W W (sad, dark) [natural minor / aeolian]
// harmonicMinor  — W H W W H 1.5 H (classical, exotic)
// melodicMinor   — W H W W W W H (jazz, sophisticated)

// === Modes ===
// dorian         — W H W W W H W (jazzy minor, soul) — minor with raised 6th
// phrygian       — H W W W H W W (dark, Spanish, metal)
// lydian         — W W W H W W H (dreamy, bright, Radiohead)
// mixolydian     — W W H W W H W (bluesy major, rock, funk)
// aeolian        — W H W W H W W (= natural minor)
// locrian        — H W W H W W W (diminished, unstable, dark)

// === Pentatonic & Blues ===
// pentatonic     — major pentatonic (W W 1.5 W 1.5) — bright, universal
// minorPentatonic — minor pentatonic (1.5 W W 1.5 W) — rock, blues, lo-fi
// blues          — minor pent + b5 (1.5 W H H 1.5 W) — blues, rock

// === Exotic ===
// wholeTone      — W W W W W W (Debussy, dreamy, floating)
// chromatic      — all half steps
// bebop          — adds passing tone to dominant scale (jazz)
// hungarianMinor — W H 1.5 H H 1.5 H (Eastern European)
// japanese       — H 1.5 W H 1.5 (koto, traditional Japanese)

